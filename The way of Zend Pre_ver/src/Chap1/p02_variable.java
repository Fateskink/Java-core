package Chap1;

public class p02_variable {
public static void main(String[] args) {
	int age = 24 ;
	int year = 2021;
	char name = 'f';
	char city = 'h';
	System.out.println("age ="+ age);	
	System.out.println("year ="+ year);	
	System.out.println("name ="+ name);	
	System.out.println("city ="+ city);
	age = 30;
	System.out.println("age ="+ age);
	city = 'n';
	System.out.println("city ="+ city);
	name = 't';
	System.out.println("name ="+ name);
}
}
